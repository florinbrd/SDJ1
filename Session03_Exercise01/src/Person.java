
public class Person {
	
	private String name; 
	private int birthday; 
	
	public void setName(String nameString) {
		
		name = nameString; 
	}
	
	public void setBirthday(int bday) {
		birthday = bday; 
	}
	
	public String getName() {
		return name; 
	}
	
	public int getBirthday() {
		return birthday; 
	}
	
	
}
