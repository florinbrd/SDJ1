
public class Person {
	
	private String name; 
	private String birthday; 
	
	public void setName(String nameString) {
		name = nameString; 
	}
	
	public void setBirthday(String bday) {
		birthday = bday; 
	}
	
	public String getName() {
		return name; 
	}
	
	public String getBirthday() {
		return birthday; 
	}

}
